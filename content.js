async function waitFor(producer) {
    const retry = async (resolve) => {
        let result = producer();
        if (result instanceof Promise) {
            result = await result;
        }
        if (result) {
            resolve(result);
        } else {
            setTimeout(() => retry(resolve), 100);
        }
    };
    return new Promise((resolve) => retry(resolve));
}

